# 5. Disease Logic (ML Integrated - CNN)
def predict_disease_logic(image_file):
    if disease_model is None or disease_classes is None:
        return "Model Not Loaded", "Please train the disease model first."
        
    try:
        # Read image to PIL and resize to match training (64x64)
        img = Image.open(image_file).convert('RGB')
        img = img.resize((64, 64))
        
        # Convert to numpy array and normalize
        img_array = np.array(img) / 255.0
        
        # Add batch dimension (model expects shape [None, 64, 64, 3])
        img_array = np.expand_dims(img_array, axis=0)
        
        # Predict
        predictions = disease_model.predict(img_array)
        class_idx = np.argmax(predictions[0])
        confidence = float(np.max(predictions[0]))
        
        result = disease_classes[class_idx]
        
        recommendations = {
            "Apple Scab": "Fungal infection. Prune affected branches and apply fungicide.",
            "Corn Common Rust": "Treat with sulfur-based fungicides and improve air circulation.",
            "Potato Early Blight": "Remove infected leaves and apply copper-based fungicides.",
            "Healthy Crop": "No issues detected. Maintain regular watering and nutrient balance."
        }
        
        msg = recommendations.get(result, "Maintain crop health and monitor closely.")
        full_msg = f"{msg} (AI Confidence: {confidence*100:.2f}%)"
        
        return result, full_msg
    except Exception as e:
        return "Error", f"Failed to process image: {e}"

