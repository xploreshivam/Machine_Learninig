# 1. Fertilizer Logic (ML Integrated)
def recommend_fertilizer_logic(n, p, k, crop):
    if fertilizer_model is None or crop_encoder is None:
        return "Model Not Loaded", "Please train the model first."
    
    # The crop coming from frontend is already formatted correctly as per Kaggle classes
    # e.g., 'Ground Nuts', 'Paddy', 'Wheat', etc.
    # Check if we have seen this crop before in our encoder
    if crop not in crop_encoder.classes_:
        return "Unknown Crop", f"Crop '{crop}' is not supported yet."
        
    encoded_crop = crop_encoder.transform([crop])[0]
    
    # The Kaggle dataset feature order: Nitrogen, Phosphorous, Potassium, Crop Type
    prediction = fertilizer_model.predict([[n, p, k, encoded_crop]])
    
    return prediction[0], "Prediction generated by AI Model based on N-P-K and Crop Type."

# 2. Crop Yield Logic (ML Integrated)
def predict_yield_logic(crop, area, rain, temp):
    if yield_model is None or yield_crop_encoder is None:
        return "Model Not Loaded", "Please train the yield model first."
        
    try:
        # Check if crop is recognized
        if crop not in yield_crop_encoder.classes_:
            return "Unknown Crop", f"Crop '{crop}' is not supported yet."
            
        encoded_crop = yield_crop_encoder.transform([crop])[0]
        
        # Ensure variables are floats
        area = float(area)
        rain = float(rain)
        temp = float(temp)
        
        # Features: Crop (encoded), Area, Rainfall, Temperature
        # Important: Order must match the X_train in train_yield.py
        prediction = yield_model.predict([[encoded_crop, area, rain, temp]])
        
        # Round prediction
        total_yield_tonnes = round(prediction[0], 2)
        
        message = f"Predicted using AI Regressor for {area} hectares of {crop}."
        return total_yield_tonnes, message
    except Exception as e:
        return "Error", f"Could not process: {e}"

# 3. Soil Quality Logic (ML Integrated)
def predict_soil_logic(n, p, k, ph):
    if soil_model is None:
        return "Model Not Loaded", "Please train the soil classifier first."
        
    try:
        n = float(n)
        p = float(p)
        k = float(k)
        ph = float(ph)
        
        # Features: Nitrogen, Phosphorus, Potassium, ph
        prediction = soil_model.predict([[n, p, k, ph]])
        
        predicted_class = prediction[0]
        
        # Generating a dynamic message
        if predicted_class == "Fertile":
            message = "Excellent! The nutrient levels and pH are optimal for agriculture."
        elif predicted_class == "Medium":
            message = "Moderate soil health. Consider mild fertilization to improve crop yield."
        else:
            message = "Poor soil quality. Strong soil treatment and fertilization are highly recommended."
            
        return predicted_class, message
    except Exception as e:
        return "Error", f"Could not process: {e}"

